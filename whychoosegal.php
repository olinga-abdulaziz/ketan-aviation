  The academy offers well furnished accommodations to its students which is available for both full time ab-initio course students as well as students coming for short term courses like recency flying, multi engine flying etc. The academies hostel is run on a no profit no loss basis with minimal charges for lodging and fooding. Separate hostel facility for boy´s and girl´s is provided. Apart from this since the airport is located within the city, there are many houses available near by where the students can live as paying guests or on minimal rent and thereby further reduce the cost of their training. There are also several hotels nearby where the students coming in for short term courses like recency flying, multi engine flying, endorsements etc. can stay and the academy will be glad to help the students to find a suitable accommodation.
