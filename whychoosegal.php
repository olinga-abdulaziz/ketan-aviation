  <p class="container" style="line-height: 1.5em; margin-top: 9px;" hidden="">
      GAL apart from having the distinction of being one of the first private flying training academy in the country also had the privilege to be among the first to start multi engine training in the country. Over the years the academy has completed more than 26,000 hrs of flying by training more than 400 students who selected us only for the reason of our commitment, honesty, transparency and foremost for the quality of our training.
      <br><br>
Unlike other academies which emphasize on flying with instructors as safety pilots, from our inception we believe that actual solo flying provides an opportunity for the students to gain self-confidence, analyse, study and correct their mistakes as also to know their limitations. This belief of the academy has received several appreciations over the years including those from senior commanders of airlines and parents of students.
<br><br>
It was because of our reputation for actual solo flying, commitment towards the training and honesty and transparency in our working, that our academy was selected by the Govt. of Jharkhand for training the ST students sponsored by the state government. Being from extremely poor families, the background of the students was not very strong and encouraging, yet the students with their hardwork and motivation of becoming pilots and uplifting the condition of their families commenced their pilot training and the academy also left no stone unturned to provide the best of best training to these students, resulting in completion of the training in a record period of time and we proudly say that immediately after completion of the course the students got placements in Air India.
<br><br>
GAL has tried to maintain the highest level of professionalism and standard of training and it is because of this that several other state governments have also selected us time and again for the training of the students sponsored by them and over the years the list of such students has become ever increasing.
<br><br>
GAL also has the distinction to be among the very few academies to have been continuously in operation for the last 16 years. Over the years numerous academies have opened and closed, but GAL has stood here ever since and has watched the aviation industry grow and flourish. The academy has even expanded and has its own Aircraft Maintenance engineering training institute with full fledged facilities and huge campus.
<br><br>
These are just a few of our achievements and milestones from a list which goes endless and it is because of our reputation and our commitment towards excellence that students have been selecting us for their flying training from as long as 1996. So go ahead and explore the flamboyant world of Aviation with the best in industry - with Garg Aviations.
</p>
<p class="container" style="line-height: 1.5em; margin-top: 9px;" hidden="">
  The academy has one of the best infrastructure in the country for pilot training which includes LCD and OHP projector equipped classrooms, labs equipped with mock-ups and cut sections of aircraft piston engines, turbine engines, landing gear, controls systems, wirings, avionics equipment etc. The facilities available with the institute are utilized to the optimum for providing the students with in depth knowledge about the aircraft and its components.
  <br><br>
At GAL our aim is to not only make you a pilot but rather an Aviator our intention and motive is to make you a qualified and learnt pilot with wide knowledge of flying so that after completion of the course the student becomes the most eligible for employment and in order to achieve these objectives our infrastructure plays a vital role.
<br><br>
Using mock-ups it becomes possible to practically demonstrate the adverse situations which a pilot might have to face and using cut sectioned components the student is able to get a look inside the functioning of each component leading to much better understanding and retention.
<br><br>
Garg Aviations has a complete facility dedicated for ground training. The facility is complete with multi-media projector equipped classrooms and separate labs for Aero-engines, Radio navigation, Electrical system, instrument system, Airframe etc. Wall charts, cutaway units, audio-visual equipment, overhead projectors, miniature models, display boards and mock-ups are provided for demonstration of principles and procedures.
<br><br>
The Ground Training Department has two Cessna 152 aircrafts dedicated solely for ground training, one Rolls Royce Dart Turbo prop engine, one Rolls Royce Avon jet engine, 4 different types of Aircraft piston engines, 4 different types of aircraft propellers and numerous other aircraft components.
<br><br>
The avionics labs (Radio navigation, Instrument and electrical system) have original aircraft avionics equipments including HF system, VHF system, VOR/ILS systems, HSI, ELT, all types of aircraft instruments and electrical equipments etc. Additionally complete original aircraft wiring layout is used to provide students with an in depth know how of all aircraft systems.
</p>
<p class="container" style="line-height: 1.5em; margin-top: 9px;" hidden="">
  The academy uses a fleet of Cessna single and multi engine aircrafts for pilot training. A description of the aircrafts used by the academy is as below:
<br><br>
<b>Cessna 152- Single Engine Abinitio/Advanced Instrument trainer:</b> The Cessna 152 is one of the worlds most favourite trainer aircraft. It has been given this title because of its excellent handling characteristics and forgiving nature. Students flying in the Cessna 152 can make big mistakes and yet be able to speak about them. It is a fixed landing gear aircraft which is one of the key requirements for ab-initio trainer and all the Cessna 152 available with GAL are IFR certified i.e. all our Cessna 152´s can be used for instrument flying. In fact our Cessna 152´s are even equipped with the most sophisticated Approach certified onboard Global Positioning system(GPS) and the latest navigation and communication equipment. Cessna 152 is also most ideal for training since it is a two seater aircraft which means it is much less expensive as compared to other four seater aircrafts used by other flying clubs and yet our Cessna 152 are equipped with all those equipments required for advanced training.
<br><br>
<b>Cessna 172- Single Engine Abinitio/Advanced Instrument trainer:</b> Cessna 172 is one of the most popular aircraft amongst the students and is widely used for flying training purposes. With more than 43,000 delivered, the Cessna 172 is a proven and reliable training aircraft. Cessna’s classic high-wing design offers many practical benefits including stable flight characteristics and increased visibility. GAL uses 172 for both ab initio and advanced flight naviagation training.
<br><br>
<b>Beechcraft Bonanza A-36- Single Advanced Complex Aircraft:</b> Beech Bonanza is one of the best aircraft available for complex training. GAL is the first private flying academy in India who has inducted such type of aircraft for providing advanced training. The aircraft is equipped with HSI, GPS,VOR/ILS, ADF, AUTO PILOT etc. which gives students a better exposure.
<br><br>
<b>Cessna 310- Multi Engine Trainer:</b> The Cessna 310 is a high performance retractable landing gear aircraft used primarily for multi engine training which apart from being a requirement of certain airlines also enables the students to expand their skills. Unlike the multi engine aircraft used by most other flying clubs in the country which have fixed landing gear, the Cessna 310 has a retractable landing gear like those installed in airline aircrafts and this enables our students to be get an exposure to advance flying skills which becomes their key USP when they apply for a pilot´s job in an airline. Our academy has been using the Cessna 310 for multi engine training for more than 14 years now and we are amongst the best for multi engine training.
<br><br>
Apart from these aircrafts, the academy is also planning to induct Cessna 172 glasscockpit and other high performance aircrafts for further advance training.
</p>
<p class="container" style="line-height: 1.5em; margin-top: 9px;" hidden="">
  As you train for a career as a pilot, you want the assurance that the planes you train in are maintained at the highest levels. At Garg Aviations, you have that guarantee. We´re an approved maintenance organisation. We don´t just meet the DGCA´s rigorous standards, we exceed them. For instance, we send all our aircraft engines, propellers and other components to their respective manufacturer in USA for overhauls even if it costs us twice as much. We believe you can never be too safe. Our maintenance team offers an extensive amount of experience with manufacturer trained engineers on hand. This means that when you fly, you can concentrate on the tasks at hand knowing your aircraft is in fine form. Even so, we never stop exploring new ways to ensure the well-being of our students. Our safety efforts continue to widen the gap between GAL and everyone else.
</p>
<p class="container" style="line-height: 1.5em; margin-top: 9px;" hidden="">
  GAL is located at Civil Aerodrome, Kanpur which is actually within the city. Most other flying training academies in the country are located several miles away from the city in the rural areas whereas GAL is located within the city and infact just 2 kms away from the main city centre.
<br><br>
Unlike other airports which have runway from 75-100 feet wide, our airport has a 3500 feet long and 150 feet wide runway which means muchgreater safety margins for the students. Kanpur also has a DVOR and NDB which allows the students to practice instrument flying. Chakeri Airport located about 5 kms from Civil Airpot also has ILS, VOR and NDB approaches which enables our students to practice precision as well as non-precision approaches.
<br><br>
Civil Airport has a dedicated and allotted airspace for flying training purposes leading to undisturbed flying throughout the year. GAL is the only operator at Civil Aerodrome, Kanpur which means that there is no traffic to interrupt the flying training activities and this means greater and better utilisation of every minute of flying time of the student. At other airports which have several operators/ traffic students mostly waste a large amount of their precious time just waiting on the ground for clearance and are unable to take the most out of their sorties.
</p>
