 Section E, Schedule II of the Aircraft Rules lays down the requirements a candidate must meet in order to be eligible for issue of a Private Pilot License, excerpts of which are as follows:
<br><br>
Requirements for issue of license– Abrn applicant for Private Pilot´s Licence shall satisfy the following requirements:
<br><br>
<b>(a) Age– </b> You shall not be less than seventeen years of age for the issue of PPL although you can obtain an SPL at 16 years of age and start your flying.
<br><br>
<b>(b) Educational Qualification– </b>You shall have passed Class Ten or equivalent Examination from a recognized Board.
<br><br>
<b>(c) Medical Fitness–</b> You shall have to undergo a Class II medical from a DGCA approved doctor and establish your fitness.
<br><br>
<b>(d) Knowledge– </b> You shall have to pass written examination in Composite (One examination comprising of subjects of Air Navigation, Aviation Meteorology and Air Regulation), Aircraft Technical General and Technical Specific (For the type of airplane intended to be flown) as per the syllabus prescribed by the Director–General.
<br><br>
<b>(e) Experience–</b> You shall have to complete at a DGCA approved flying training academy, flying training on an aeroplane of not less than forty hours of flight time which shall include–
<br><br>
(1) not less than twenty hours of solo flight time;
<br><br>
(2) not less than five hours of cross-country flight time in accordance with para 5(b) of Section A as the sole occupant of an aeroplane including a flight of not less than one hundred and fifty nautical miles in the course of which full stop landings at two different aerodromes shall have been made;
<br><br>
(3) not less than ten hours of solo flight time completed within a period of twelve months immediately preceding the date of application for the issue of license;
<br><br>
<b>Privileges– </b>Subject to the validity of endorsements and ratings in the licence and compliance with the relevant provisions of Rules 39-B, 39-C and 42, the privileges of the holder of a Private Pilot´s Licence shall be to act, but not for remuneration, as Pilot-on-command or as Co-pilot of any aeroplane which is entered in the Aircraft Rating of his licence and carry passengers therein :
<br><br>
Provided that–
<br><br>
(1) No flight is undertaken for hire or remuneration of any kind, whatsoever;
<br><br>
(2) The passengers are carried by night only when the holder of licence is in possession of a valid night rating and has carried out not less than five take-offs and five landings by night as Pilot-in-command within a period of six months immediately preceding the date of intended flight;
<br><br>
(3) No flight shall be undertaken under the Instrument Flight Rules without being in possession of a valid Instrument Rating. NOTE: Although the minimum flight time for issue of PPL is 40 hrs but invariably almost all students require about 60 hrs of training before they are proficient for the issue of PPL.
  </p>
