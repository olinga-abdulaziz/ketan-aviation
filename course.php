  	Learning to fly is a personal challenge that offers a chance to do something really different. The process can teach you as much about yourself and your skills as about flying an aircraft. You will develop improved self discipline and self-analysis as you learn to safely operate these machines.
<br><br>
Learning to fly is not as hard as you might think. In fact, each year many people of all ages and from all walks of life learn to fly. You can start learning as early as when you are 16 years old, though you must be 17 yrs to obtain a private pilot´s licence (PPL) and 18 yrs to obtain a Commercial Pilot´s License (CPL).
<br><br>
Garg Aviation offers flight training for all DGCA licenses, from private pilot through flight instructor and airline transport pilot, in a professional environment. The training can be conducted full time or part time depending on your schedule.
<br><br>
To receive your private pilot license, you will have to take flight training, gain solo flight experience, a written exam and a practical test. Similar Is the case for other courses although as the importance of the license increases so does the standards on which the training is required to be given. Although it is not necessary to have a Private pilot license in order to get your commercial pilot license, however from our experience we suggest that every student undergoing CPL training must first obtain his PPL then proceed on to for clearing the CPL written examinations. That way you will have a good idea of the examination system and having a PPL also gives much more confidence and privileges.
<br><br>
During your training some of the things you will learn are general handling of the airplane (straight and level flight, climbs, descents, turns, airspeed changes, slow flight, steep turns and stalls), take offs and landings, visual and radio navigation, radio communications, cross country flying, and emergency procedures.
<br><br>
After about 15 hours of flight training you will probably be ready for your first solo flight. In addition to your flight training you also have to take written exams which are conducted by the DGCA every three months and there is no restriction as to the number of times you can appear in these written exams. You will receive ground instruction in conjunction with your flight training, and much of that instruction will also be helpful in preparing you for the written exams. The best time to take the written exams is when your flight training is about 50% completed. This way you will be able to use the practical experience you have gained during your flight training to help you with the written exams. If you have difficulty with any of the study material, your instructor can help you with it.
  </p>
