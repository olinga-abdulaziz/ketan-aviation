<b>3. Capt. Rishabh Garg, Chief Flying Instructor/Examiner:</b> Capt. Rishabh Garg is a holder of Commercial Pilot License as well as Flight Instructor Rating. He has more than 3500 hours of flying experience in eight different types of aircraft including multi engine aircraft. Capt. Rishabh is examiner/instructor on single and multi engine airplanes and is also pursuing Aircraft Maintenance Engineer License course. He is looking after the day to day working and ensures smooth operation of flying training. He also has Open rating to fly all single engine aircraft with maximum weight up to 1500 kgs. and has a passion of getting more and more aircrafts endorsed on his license.
