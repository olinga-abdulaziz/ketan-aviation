	<p class="container" style="line-height: 1.5em; margin-top: 9px;" hidden="">
			Garg Aviations came into existence in the year 1995. The idea of the academy was conceived by Capt. Vijay Kumar Garg who started his flying at the Govt. Flying Training Centre (GFTC) , Kanpur in the year 1984 and he obtained his Private Pilot license the same year followed by the Certificate of Proficiency in Radio Telephony Restricted (Aeromobile) in the next year. However the GFTC, Kanpur was closed down because of which Mr.Garg faced great difficulties in completing his flying and it was because of his own suffering and inability to complete his flying due to the non-availability of good flying training academies in the country that Mr. Garg decided to start his own flying training academy so that the future generations do not have to suffer the same hardships which he had to go through in fulfilling his dream of becoming a pilot and thus Garg Aviation became the first academy in the country " for pilots by pilots", in the true sense.
				<br><br>
Garg Aviations was one of the first private flying training academy in the country and for over 14 years was the only fully functional flying training academy in northern India. Garg Aviations also has the distinction of commencing multi engine training for students at a time when only a very few academies in the country had multi engine aircrafts and now it has been over 23 years that Garg Aviations has been providing multi engine training to aspiring pilots with a reputation which even attracts the experienced pilots from the Indian Airforce, Navy and coast guard to come to GAL for their training for the issue of Airline Transport Pilot License.	
		<br></br>
	</p>
	<p class="container" style="line-height: 1.5em; margin-top: 9px;" hidden="">	
			GAL operates from Civil Airport, Kanpur which is located inside the cantonment area, in the heart of Kanpur city and just minutes away from the main city center which is unlike almost all other flying training academies in the country who are located several miles away from the city.
<br><br>	
Civil airport, Kanpur being uncontrolled offers the most suitable training environment for abi-nitio flying training and its proximity to Lucknow International Airport and Chakeri domestic Airport enables the students to obtain the much needed experience of flying in a busy environment and exposure to instrument flying procedures and instrument approaches in the advanced stages of training. The ILS,DVOR,DME and NDB navigation facilities at Kanpur are located 3 Nautical Miles from Civil Airport and thus provide students with exposure to latest navigational aids. The East-West oriented runway at Civil Airport, Kanpur is 3550 ft long and 150 ft wide which is much above the minimum requirement for flying training operations and provides much higher safety margin for ab-initio flying training meaning much greater safety of our most valued possessions, our students.
<br><br>	
The airport being located in the clean and calm area of cantonment offers an unmatched learning environment along with the safety of students which has had great contribution to the success of numerous students who have passed out of our academy over the years. Also the airport is very close to the Mall Road and other major attractions of Kanpur which provides students several options for recreation and a break from their studies.
	</p>
