	<p class="container" style="line-height: 1.5em; margin-top: 9px;">	
		Garg Aviations Limited (GAL) is a flight training academy established in the year 1995 at Civil Airport, Kanpur and engaged in conducting Director General of Civil Aviation, Govt. of India approved flying training courses. The academy has trained numerous students from all over India including candidates from Air force, Navy and Coast guard for obtaining their Private Pilot License/Commercial Pilot License/Airline Transport Pilot License/Instrument rating/ Multi engine rating/Instructor rating etc. The students trained by GAL include self sponsored candidates as well as students sponsored by the government.
			<br>	<br>
&nbsp;&nbsp; Unlike other academies which are new, relatively inexperienced and can only give probability of completing training in a short duration of time, GAL has actually trained students in record durations of as less as 10 months while at the same time maintaining the highest possible standards of training and education. For the last 23 years the academy has given high emphasis on providing as much actual solo flying to students as possible so as to boost their confidence and enable them to evolve their skills and know their limitations. With the help of some of the industry´s most experienced and qualified flying and ground instructors, the students are able to obtain the most from their training and it is our reputation for this completely balanced training with high emphasis on actual solo flying that almost all our students have been able to find placement in various airlines through the years and even in times of recession.
		<br><br>
GAL offers fully integrated courses which are supplemented by our fleet of Cessna single and multi engine aircrafts equipped with the most modern avionics including Global positioning system and advanced navigation and communication equipments which enables our students to perfectly learn the skills required for their job as a pilot. All training including flying and ground classes are conducted in house at Civil Aerodrome, Kanpur itself and therefore unlike other academies our students do not have to wander different cities for their training.
		<br><br>
GAL produces some of the industry´s best pilots. We work to ensure steady flow of quality-trained young pilots into the flight decks of airlines and corporate aircraft nationwide. And we train to meet and exceed current industry standards.
	</p>
	<p class="container" style="line-height: 1.5em; margin-top: 9px;" hidden="">
		Over the years the academy has had the privilege to have the association of some of the best minds in the Indian Aviation industry, beginning with Wg. Cdr B.K. Dhiman, Former Chief Test Pilot, Hindustan Aeronautics Limited, IAF Fighter Pilot, Qualified Flight Instructor and ATPL holder who was the academy’s first Chief Ground Instructor and Capt. Arun Mann, India’s most renowned Chief Flying Instructor with over 10,000 hrs of flying experience who served the academy as its Chief Flying Instructor for over 14 years until he was selected by the DGCA for the post of Director of Flying Training for the country. Similar is the case with the engineering department of the academy which has always remained in the hands of some of the best Aircraft Maintenance Engineers (AME) in the country like Shri B.P. Singh, holder of AME license in category A, B, C, D and X, former Chief Engineer of Hind Provincial Flying Club, UP state government Civil Aviation Department, IIT- Kanpur-Aerospace Department and other such organisations and Shri M. C. De Sarkar, holder of AME license in category A, B, C and X who was the first AME licensed to certify the Beechcraft Turbine engine aircraft in India and he was also the former Chief Engineer of the Ford Foundation- India, U.P. State government, Hind Provincial Flying Club and other such organisations of high repute.
		<br><br>	

<b>The present team also consists of some of the best in the Aviation industry. The profiles of key personnel of the academy are as below:
</b>
<b>1. Capt. Vijay Kumar Garg, Accountable Manager:</b>Mr. Garg is the Accountable Manager of the academy and looks over the entire functioning and day to day working of the academy. Mr. Garg has a Masters in Commerce and a Bachelor of Law. He is also holder of a Private Pilot License as also Airframe and Powerplant License issued by the Federal Aviation Administration, USA. Mr. Garg has more than 35 years of experience in the aviation sector and more than 24 years of experience in aircraft maintenance. Mr. Garg also has the distinction of being the only person in the country to have received comprehensive and specialized manufacturer training from both Teledyne Continental Motors as well as Textron Lycoming, two of the world’s most renowned manufacturers of Aircraft engines.
<br><br>	
<b>2. Capt. Mayank Garg, Chief Flying Instructor:</b> Capt. Mayank has more than 6000 hrs of flying experience including more than 5500 hours as instructor. Capt. Mayank did his initial flying training at GAL. He has also received specialized multi engine, complex aircraft and instrument flying training in USA and is also the holder of FAA, USA Airline Transport Pilot license with multi engine and instrument ratings. He has also cleared FAA, USA examinations on fundamentals of Flying Instructions, Certified Flight Instructor, Certified Flight Instructor-Instrument. Capt. Mayank has also attended training in USA on Beechcraft Bonanza A36, Piper Saratoga PA32R-301, Piper Seneca PA-34 and Beech Duchess aircraft which makes him qualified to fly on nine different types of aircraft including four different multi engine aircraft. He also has Open rating to fly all single engine aircraft with maximum weight up to 1500 kgs.
